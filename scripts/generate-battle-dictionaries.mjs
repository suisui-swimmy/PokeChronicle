import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");

const SOURCES = {
  pokemon: resolve(repoRoot, "others/pokemon-names/pokemon-names-ja-ui.txt"),
  moves: resolve(repoRoot, "others/move-names/move-names-ja-ui.txt"),
};

const OUTPUTS = {
  ts: resolve(repoRoot, "src/core/dictionary/generatedBattleDictionary.ts"),
  pokemonJson: resolve(repoRoot, "data/dictionaries/pokemon.generated.json"),
  movesJson: resolve(repoRoot, "data/dictionaries/moves.generated.json"),
};

function readNameList(path) {
  const seen = new Set();
  const entries = [];
  const lines = readFileSync(path, "utf8").replace(/^\uFEFF/, "").split(/\r?\n/);

  for (const [index, line] of lines.entries()) {
    const label = line.trim();

    if (!label || seen.has(label)) {
      continue;
    }

    seen.add(label);
    entries.push({
      sourceLine: index + 1,
      label,
    });
  }

  return entries;
}

function toDictionaryEntries(kind, names) {
  return names.map((entry, index) => ({
    id: `${kind}:${String(index + 1).padStart(4, "0")}`,
    label: entry.label,
  }));
}

function stripTrailingGenderMarker(label) {
  const base = label.replace(/[♀♂]$/u, "");
  return base === label ? null : base;
}

function expandPokemonNames(names) {
  const seen = new Set();
  const expanded = [];

  function add(label, entry) {
    if (seen.has(label)) {
      return;
    }

    seen.add(label);
    expanded.push({
      ...entry,
      label,
    });
  }

  for (const entry of names) {
    const genderlessBase = stripTrailingGenderMarker(entry.label);

    if (genderlessBase) {
      add(genderlessBase, entry);
    }

    add(entry.label, entry);
  }

  return expanded;
}

function writeJson(path, entries) {
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, `${JSON.stringify(entries, null, 2)}\n`, "utf8");
}

function formatTsArray(name, entries) {
  return `export const ${name}: readonly DictionaryEntry[] = ${JSON.stringify(entries, null, 2)};\n`;
}

const pokemonEntries = toDictionaryEntries(
  "pokemon",
  expandPokemonNames(readNameList(SOURCES.pokemon)),
);
const moveEntries = toDictionaryEntries("move", readNameList(SOURCES.moves));

const ts = `// Generated by scripts/generate-battle-dictionaries.mjs. Do not edit manually.\n` +
  `// Sources are local reference files under others/ and are not read at runtime.\n` +
  `import type { DictionaryEntry } from "./types";\n\n` +
  formatTsArray("POKEMON_NAME_DICTIONARY", pokemonEntries) +
  `\n` +
  formatTsArray("MOVE_NAME_DICTIONARY", moveEntries) +
  `\n` +
  `export const BATTLE_DICTIONARY = {\n` +
  `  pokemon: POKEMON_NAME_DICTIONARY,\n` +
  `  moves: MOVE_NAME_DICTIONARY,\n` +
  `} as const;\n`;

mkdirSync(dirname(OUTPUTS.ts), { recursive: true });
writeFileSync(OUTPUTS.ts, ts, "utf8");
writeJson(OUTPUTS.pokemonJson, pokemonEntries);
writeJson(OUTPUTS.movesJson, moveEntries);

console.log(
  `Generated ${pokemonEntries.length} pokemon names and ${moveEntries.length} move names.`,
);
